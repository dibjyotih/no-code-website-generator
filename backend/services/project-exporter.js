/**
 * Export a complete full-stack project as a downloadable structure
 * @param {Object} project - Project data with components, schema, routes, etc.
 * @returns {Promise<Object>} File structure object
 */
export async function exportFullStackProject(project) {
  const {
    name,
    components,
    databaseSchema,
    apiRoutes,
    authConfig,
    settings,
  } = project;

  const framework = settings.framework || 'nextjs';
  
  const projectStructure = {
    'package.json': generatePackageJson(name, framework),
    '.env.example': generateEnvExample(authConfig, databaseSchema),
    'README.md': generateReadme(name, framework),
    '.gitignore': generateGitignore(),
    'tsconfig.json': generateTsConfig(),
  };

  // Add framework-specific files
  if (framework === 'nextjs') {
    Object.assign(projectStructure, generateNextJSFiles(components, apiRoutes, authConfig));
  } else if (framework === 'vite') {
    Object.assign(projectStructure, generateViteFiles(components));
  }

  // Add database files if schema exists
  if (databaseSchema) {
    projectStructure['prisma/schema.prisma'] = databaseSchema.prismaSchema;
    projectStructure['prisma/migrations/init.sql'] = databaseSchema.sqlMigration;
  }

  // Add deployment config
  projectStructure['vercel.json'] = generateVercelConfig();
  projectStructure['.env.production.example'] = generateProductionEnv();

  return projectStructure;
}

