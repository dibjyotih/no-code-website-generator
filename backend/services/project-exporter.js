function generateNextJSFiles(components, apiRoutes, authConfig) {
  const files = {};

  // App layout
  files['app/layout.tsx'] = `import './globals.css';
import type { Metadata } from 'next';

export const metadata: Metadata = {
  title: 'WebWeaver Generated App',
  description: 'Generated by WebWeaver AI',
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html lang="en">
      <body>{children}</body>
    </html>
  );
}
`;

  // Home page
  files['app/page.tsx'] = `import React from 'react';
${components.map((c, i) => `import Component${i} from '@/components/${c.name}';`).join('\n')}

export default function Home() {
  return (
    <main>
      ${components.map((c, i) => `<Component${i} />`).join('\n      ')}
    </main>
  );
}
`;

  // Components
  components.forEach((component, index) => {
    files[`components/${component.name}.tsx`] = component.code;
  });

  // API Routes
  if (apiRoutes && apiRoutes.length > 0) {
    apiRoutes.forEach(route => {
      const filename = route.path.replace(/^\/api\//, '').replace(/:/g, '[') + '].ts';
      files[`app/api/${filename}`] = `import { NextRequest, NextResponse } from 'next/server';

${route.code}

export async function ${route.method}(req: NextRequest) {
  ${route.code}
}
`;
    });
  }

  // Global CSS
  files['app/globals.css'] = `@tailwind base;
@tailwind components;
@tailwind utilities;

* {
  box-sizing: border-box;
  margin: 0;
  padding: 0;
}

body {
  font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif;
}
`;

  return files;
}

function generateViteFiles(components) {
  const files = {};

  files['index.html'] = `<!DOCTYPE html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>WebWeaver Generated App</title>
  </head>
  <body>
    <div id="root"></div>
    <script type="module" src="/src/main.tsx"></script>
  </body>
</html>
`;

  files['src/main.tsx'] = `import React from 'react';
import ReactDOM from 'react-dom/client';
import App from './App';
import './index.css';

ReactDOM.createRoot(document.getElementById('root')!).render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
`;

  files['src/App.tsx'] = `import React from 'react';
${components.map((c, i) => `import Component${i} from './components/${c.name}';`).join('\n')}

export default function App() {
  return (
    <div className="App">
      ${components.map((c, i) => `<Component${i} />`).join('\n      ')}
    </div>
  );
}
`;

  // Components
  components.forEach(component => {
    files[`src/components/${component.name}.tsx`] = component.code;
  });

  files['vite.config.ts'] = `import { defineConfig } from 'vite';
import react from '@vitejs/plugin-react';

export default defineConfig({
  plugins: [react()],
});
`;

  return files;
}

function generateEnvExample(authConfig, databaseSchema) {
  let env = `# Database
DATABASE_URL="postgresql://user:password@localhost:5432/dbname"

# API Configuration
API_URL=http://localhost:3000/api
`;

  if (authConfig?.enabled) {
    env += `
# Authentication
NEXTAUTH_SECRET=your-secret-key-here
NEXTAUTH_URL=http://localhost:3000
`;

    if (authConfig.provider === 'nextauth') {
      env += `
# OAuth Providers (optional)
GOOGLE_CLIENT_ID=
GOOGLE_CLIENT_SECRET=
GITHUB_ID=
GITHUB_SECRET=
`;
    }
  }

  return env;
}

